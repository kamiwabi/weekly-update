var a:number = 1
var b:number = 3
 
switch (a+b){
    case 1 : {
        console.log("a+b is 1.")
        break
    }
    case 3 : {
        console.log("a+b is 3.")
        break
    }
    case 4 : {
        console.log("a+b is 4.")
        break
    }
    case 5 : {
        console.log("a+b is 5.")
        break
    }
    default : {
        console.log("a+b is 5.")
        break
    }
}